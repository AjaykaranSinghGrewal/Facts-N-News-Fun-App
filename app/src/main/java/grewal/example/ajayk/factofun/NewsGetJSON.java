public class NewsGetJSON {
    private String  mNewsTitle;
    private String mNewsDescription;
    private String mNewsPublished;
    private String mNewsLogo;

    public String getNewsTitle() {
        return mNewsTitle;
    }

    public void setNewsTitle(String mNewsTitle) {
        this.mNewsTitle = mNewsTitle;
    }

    public String getNewsDescription() {
        return mNewsDescription;
    }

    public void setNewsDescription(String mNewsDescription) {
        this.mNewsDescription = mNewsDescription;
    }

    public String getNewsPublished() {
        return mNewsPublished;
    }

    public void setNewsPublished(String mNewsPublished) {
        this.mNewsPublished = mNewsPublished;
    }

    public String getNewsLogo() {
        return mNewsLogo;
    }

    public void setNewsLogo(String mNewsLogo) {
        this.mNewsLogo = mNewsLogo;
    }
}
